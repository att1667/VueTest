<template>
    <div>
        <div>{{data.writer}}</div>
        <div>{{data.title}}</div>
        <div>{{data.content}}</div>
        <button @click="modifyData">수정</button>
        <button @click="deleteData">삭제</button>
    </div>
</template>

<script>
import data from '@/data'

export default {
    name: 'Detail',
    data() {
        const index = this.$route.params.contentId //$route 현재 url
        console.log(index)
        return {
            data: data[index]
        }
    },
    methods: {
        deleteData() {
            data.splice(this.index, 1)
            this.$router.push({
                path: '/'
            })
        },
        modifyData() {
            this.$router.push({
                name: 'Modify',
                param: {
                    contentId: this.index
                }
            })
        }
    }
}
</script>